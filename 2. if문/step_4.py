# 2884 문제
# URL : https://www.acmicpc.net/problem/2884

a,b = input().split(" ")
a,b = (int(a), int(b))

time = ((a*60 + b) - 45)//60
minute = ((a*60 + b) - 45)%60

if time == -1:
    print("23", end=" ")
    print(minute)
else:
    print(time, end=" ")    
    print(minute)    

